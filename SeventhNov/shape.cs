using System;
using System.Collections.Generic;
using System.Text;

namespace SeventhNov
{
    public class shape
    {
        public double circle(int a)
        {
            return 2 * 3.14 * a;
        }

    }

    public class Area
    {
        public double traingle(int h, int b)
        {
            return 0.5 * h * b;
        }
    }

    public class perimeter
    {
        public double rectangle(int l, int w)
        {
            return 2 * (l + w);
        }
    }


    
}
